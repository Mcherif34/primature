package ma.brainit.base;

public class CountEntity {

	private Long nombre;
	private String nature;

	public CountEntity() {
		super();
	}

	public CountEntity(Long nombre, String nature) {
		super();
		this.nombre = nombre;
		this.nature = nature;
	}

	public Long getNombre() {
		return nombre;
	}

	public void setNombre(Long nombre) {
		this.nombre = nombre;
	}

	public String getNature() {
		return nature;
	}

	public void setNature(String nature) {
		this.nature = nature;
	}

}